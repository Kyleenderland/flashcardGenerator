public class Flashcard
{
  private String question;
  private String answer;

  public void setQuestion( String s )
  {
    question = s;
  } //end 'setQuestion'

  public void setAnswer( String s )
  {
    answer = s;
  } //end 'setAnswer'

  public String getQuestion()
  {
    return question;
  }

  public String getAnswer()
  {
    return answer;
  }

} //end 'Flashcards'
